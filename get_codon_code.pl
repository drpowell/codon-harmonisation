  my($amino, $codon, $num) = ($1,$2,$3);
  die "DUPLICATE. Re-write program!" if exists($r->{$num});
  $r->{$num} = [$amino, $codon];
}

for my $i (0 .. $#counts) {
  printf "%2d: %s %s\n", $i, $r->{$counts[$i]}[1], $r->{$counts[$i]}[0];
}

